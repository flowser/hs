     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $organisation = Auth::user()->organisationemployees()->first();
        $directors = User::with('roles','permissions','counties', 'counties', 'constituencies', 'wards','organisationdirectors')
                        ->whereHas('organisationdirectors', function($query) use($organisation)
                                {
                                $query ->where('organisation_id', $organisation->id);
                                }
                            )
                            ->get();
        return response()-> json([
            'directors'=>$directors,
        ], 200);
    }

